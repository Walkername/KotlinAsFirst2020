fun minDivisor(n: Int): Int {
    var m = 2
    if (isPrime(n)) return n
    while (n % m != 0) m++
    return m
}

/**
 * Простая (2 балла)
 *
 * Для заданного числа n > 1 найти максимальный делитель, меньший n
 */
fun maxDivisor(n: Int): Int {
    var m = n / 2
    if (isPrime(n)) return 1
    while (n % m != 0) {
        m--
    }
    return m
}

/**
 * Простая (2 балла)
 *
 * Гипотеза Коллатца. Рекуррентная последовательность чисел задана следующим образом:
 *
 *   ЕСЛИ (X четное)
 *     Xслед = X /2
 *   ИНАЧЕ
 *     Xслед = 3 * X + 1
 *
 * например
 *   15 46 23 70 35 106 53 160 80 40 20 10 5 16 8 4 2 1 4 2 1 4 2 1 ...
 * Данная последовательность рано или поздно встречает X == 1.
 * Написать функцию, которая находит, сколько шагов требуется для
 * этого для какого-либо начального X > 0.
 */
fun collatzSteps(x: Int): Int {
    var k = 0
    var l = x
    while (l != 1) {
        if (l % 2 == 0) l /= 2 else l = 3 * l + 1
        k++
    }
    return k
}

/**
 * Средняя (3 балла)
 *
 * Для заданных чисел m и n найти наименьшее общее кратное, то есть,
 * минимальное число k, которое делится и на m и на n без остатка
 */
fun lcm(m: Int, n: Int): Int {
    val l = if (m > n) m else if (n > m) n else m
    var k = when {
        (m % n != 0) && (m > n) -> m
        (n % m != 0) && (n > m) -> n
        else -> n
    }
    while ((k % n != 0) || (k % m != 0)) {
        k += l
    }
    return k
}

/**
 * Средняя (3 балла)
 *
 * Определить, являются ли два заданных числа m и n взаимно простыми.
 * Взаимно простые числа не имеют общих делителей, кроме 1.
 * Например, 25 и 49 взаимно простые, а 6 и 8 -- нет.
 */
fun isCoPrime(m: Int, n: Int): Boolean {
    var l = 0
    var s = if (m >= n) n else m
    for (k in 2..s) {
        if (isPrime(m) && isPrime(n) && m % n != 0 && n % m != 0) return true
        if (m % k == 0 && n % k == 0 || m % n == 0 || n % m == 0) l++
        if (m <= 2 || n <= 2) l = 0
        if (l != 0) return false
    }
    return l == 0
}

/**
 * Средняя (3 балла)
 *
 * Для заданных чисел m и n, m <= n, определить, имеется ли хотя бы один точный квадрат между m и n,
 * то есть, существует ли такое целое k, что m <= k*k <= n.
 * Например, для интервала 21..28 21 <= 5*5 <= 28, а для интервала 51..61 квадрата не существует.
 */
fun squareBetweenExists(m: Int, n: Int): Boolean {
    var k = 0
    if (m == n && sqr(k) == m) return true
    while (k < sqrt(Int.MAX_VALUE.toDouble())) {
        if (sqr(k) in m..n) return true
        k++
    }
    return false
}

